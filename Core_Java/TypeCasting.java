public class TypeCasting {
    public static void main(String[] args){
        byte b =127;
        int a = 12;
        byte k = (byte)a;
 
        float f = 5.6f;
        int t = (int)f;
        int num=10;
        int result = ++num;
        System.out.println(result);
        System.out.println(k);
        System.out.println(t);
        System.out.println(num);

    }
}
