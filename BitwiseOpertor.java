
package javaBeginning;


public class BitwiseOpertor {
    public static void main (String [] args){
    int x=32;
    int y=12;
    int c;
    c= x&y;
    System.out.println("x & y = "+c);
    c= x|y;
    System.out.println("x | y = "+c);
    c= x^y;
    System.out.println("x ^ y = "+c);
    c= x>>2;
    System.out.println("x & y = "+c);
    c= x<<2;
    System.out.println("x & y = "+c);
    
    }
}
