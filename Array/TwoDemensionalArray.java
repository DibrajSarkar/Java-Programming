
package javaBeginning.Array;


public class TwoDemensionalArray {
    public static void main(String[] args) {
        int [][]num=new int[2][3];
        num[0][0]=12;
        num[0][1]=12;
        num[0][2]=22;
        num[1][0]=12;
        num[1][1]=12;
        num[1][2]=12;
        System.out.println(num[0][2]);
    }
}
